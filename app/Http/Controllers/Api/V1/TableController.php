<?php

namespace App\Http\Controllers\Api\V1;

use App\Models\Table;
use App\Http\Requests\StoreTableRequest;
use App\Http\Requests\UpdateTableRequest;
use App\Http\Controllers\Controller;
use App\Http\Resources\V1\TableResource;
use App\Http\Resources\V1\TableCollection;
use App\Filters\V1\TableFilter;
use Illuminate\Http\Request;

class TableController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $filter = new TableFilter();
        $queryItems = $filter->transform($request);
        if (count($queryItems) == 0) {
            return new TableCollection(Table::paginate());
        } else {
            $items = Table::where($queryItems)->paginate();
            return new TableCollection($items->appends($request->query()));
        }
    }

